def search_pidgin(query: str) -> dict:
    """
    Search the Nigerian Pidgin dictionary for words or phrases
    containing the query string (case-insensitive).

    Args:
        query: The word or phrase to search for.

    Returns:
        A dictionary of matching Pidgin entries and their English meanings.
    """
    query_lower = query.lower()
    return {
        word: meaning
        for word, meaning in nigerian_pidgin.items()
        if query_lower in word.lower() or query_lower in meaning.lower()
    }


def get_entry(pidgin_word: str) -> str:
    """
    Look up a single Pidgin word or phrase.

    Args:
        pidgin_word: The Pidgin word or phrase to look up.

    Returns:
        The English meaning, or a 'not found' message.
    """
    return nigerian_pidgin.get(
        pidgin_word.lower(),
        f"'{pidgin_word}' not found in the dictionary.",
    )


