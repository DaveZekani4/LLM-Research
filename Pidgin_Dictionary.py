# QUICK LOOKUP HELPERS
# =============================================================================

def lookup(word: str) -> PidginEntry | None:
    """Case-insensitive lookup of a Pidgin word or phrase."""
    return NAIJA_PIDGIN.get(word.lower())


def search_by_tone(tone: str) -> list[str]:
    """Return all words that carry a given tone (e.g. 'insult', 'humour')."""
    results = []
    for word, entry in NAIJA_PIDGIN.items():
        tones = entry["tone"]
        if isinstance(tones, list):
            if tone.lower() in [t.lower() for t in tones]:
                results.append(word)
        elif tone.lower() in tones.lower():
            results.append(word)
    return sorted(results)


def search_by_region(region: str) -> list[str]:
    """Return all words strongly associated with a given region."""
    return sorted([
        word for word, entry in NAIJA_PIDGIN.items()
        if region.lower() in entry["region"].lower()
    ])


def search_by_pos(pos: str) -> list[str]:
    """Return words matching a part-of-speech substring (e.g. 'proverb', 'noun')."""
    return sorted([
        word for word, entry in NAIJA_PIDGIN.items()
        if pos.lower() in entry["pos"].lower()
    ])


def search_english(keyword: str) -> list[str]:
    """Return Pidgin words whose English translation contains the keyword."""
    results = []
    for word, entry in NAIJA_PIDGIN.items():
        en = entry["english"]
        if isinstance(en, list):
            if any(keyword.lower() in e.lower() for e in en):
                results.append(word)
        elif keyword.lower() in en.lower():
            results.append(word)
    return sorted(results)


def random_word() -> tuple[str, PidginEntry]:
    """Return a random entry from the dictionary."""
    import random
    word = random.choice(list(NAIJA_PIDGIN.keys()))
    return word, NAIJA_PIDGIN[word]


def stats() -> dict:
    """Print basic statistics about the dictionary."""
    total = len(NAIJA_PIDGIN)
    by_formality = {}
    by_region = {}
    for entry in NAIJA_PIDGIN.values():
        f = entry["formality"]
        by_formality[f] = by_formality.get(f, 0) + 1
        r = entry["region"].split("/")[0].strip()
        by_region[r] = by_region.get(r, 0) + 1
    return {
        "total_entries": total,
        "by_formality": by_formality,
        "top_regions": dict(sorted(by_region.items(), key=lambda x: -x[1])[:5]),
    }


